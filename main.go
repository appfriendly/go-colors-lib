package colors

func reset() string {
	return "\033[0m"
}

func red() string {
	return "\033[31m"
}

func green() string {
	return "\033[32m"
}

func yellow() string {
	return "\033[33m"
}

func blue() string {
	return "\033[34m"
}

func purple() string {
	return "\033[35m"
}

func cyan() string {
	return "\033[36m"
}

func grey() string {
	return "\033[37m"
}

func white() string {
	return "\033[97m"
}
